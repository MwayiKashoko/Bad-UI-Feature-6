let assetsLoaded = false;
export const IMAGES = {};
export const AUDIOS = {};

export function preloadAssets() {
  if (assetsLoaded) return;
  assetsLoaded = true;

  imagePaths.forEach(path => {
    const img = new Image();
    img.src = path;
    IMAGES[path] = img;
  });

  audioPaths.forEach(path => {
    const audio = new Audio(path);
    AUDIOS[path] = audio;
  });
}